/**
 * @brief 读取电压值
 * @retval 电压值
 */
float Get_VolValue(void)
{
  uint32_t ADC_Value = 0;// 定义AD值
  float VolValue = 0;// 定义电压值
  HAL_ADC_Start(&hadc1);                 // 启动ADC转换
  HAL_ADC_PollForConversion(&hadc1, 50); // 等待转换完成，50为最大等待时间，单位为ms
  if (HAL_IS_BIT_SET(HAL_ADC_GetState(&hadc1), HAL_ADC_STATE_REG_EOC)) // 检查ADC状态和结束标志位
  {
    ADC_Value = HAL_ADC_GetValue(&hadc1); // 获取AD值
    VolValue = (ADC_Value * 3.133f) / 4095.0f; // 根据内部参考电压 3.133V 计算电压值，4095为12位AD值
  }
  return VolValue;
}
